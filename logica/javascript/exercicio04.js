// Crie um script que use um loop (for ou while) para iterar de 1 a 20.
// Para cada número, imprima se ele é "Par" ou "Ímpar" no console.
//
for (let i = 1; i <= 20; i++) {
  if (i % 2 == 0) {console.log(`o numero ${i} é par.`);}
  else {console.log(`o numero ${i} é impar`);}
}