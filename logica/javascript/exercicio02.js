// Crie um script que declare duas variáveis numéricas.
// Em seguida, realize as operações básicas (soma, subtração, multiplicação, divisão)
// entre elas e imprima o resultado de cada operação no console.
//
// **Exemplos:**
// num1 = 10, num2 = 5
// Soma: 15
// Subtração: 5
// Multiplicação: 50
// Divisão: 2

let numero1 = 1;
let numero2 = 2;

let soma = numero1 + numero2;
let subtracao = numero1 - numero2;
let multiplicacao = numero1 * numero2;
let divisao = numero1 / numero2;

console.log(`A soma de ${numero1} e de ${numero2} é ${soma}`);
console.log(`A subtração de ${numero1} e de ${numero2} é ${subtracao}`);
console.log(`A multiplicação de ${numero1} e de ${numero2} é ${multiplicacao}`);
console.log(`A divisão de ${numero1} e de ${numero2} é ${divisao}`);
