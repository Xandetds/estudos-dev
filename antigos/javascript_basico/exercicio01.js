// Meu primeiro código de JavaScript!
console.log("hello world");

// Declaração e uso de variáveis com 'let'
let name = 'xande';
console.log(name);

// Regras importantes para nomes de variáveis em JavaScript:
// - Não pode ser uma palavra chave da linguagem (ex: 'let', 'function', 'if').
// - Deve ter um significado claro e descritivo.
// - Não pode começar com um número (ex: '1name' é inválido).
// - Não pode conter espaços ou traços (ex: 'first-name' é inválido).
//   A convenção mais usada em JavaScript para múltiplas palavras é 'camelCase' (ex: 'firstName').

let firstName = 'Alexandre';
let lastName = 'Tibes da Silva';
console.log('Nome completo: ' + firstName + ' ' + lastName);